package com.example.demo;


public class Atencion {

    private int id;
    private String descripcion;

    
    public Atencion(int id, String descripcion) {
        this.id = id;
        this.descripcion = descripcion;
    }

    // Getters
    public int getId() {
        return id;
    }

    public String getDescripcion() {
        return descripcion;
    }

    
    
}
